public class Assignment2 {
    public static void main(String[] args){
        int no[][]={{1,2,3},{4,5,6},{7,8,9}};
        int sum=0, n=0, ro=0;
        for (int i=0;i<no.length;i++){
            sum=0;
            for (int j=0;j<no[i].length;j++){
                sum+=no[i][j];
            }
            if (sum>n){
                n=sum;
                ro=i;
            }
        }

        for (int j=0;j<no[ro].length;j++){
            System.out.print(no[ro][j]+" ");
        }
    }
}